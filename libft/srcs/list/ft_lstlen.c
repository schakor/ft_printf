#include "libft.h"

size_t	ft_lstlen(t_list *lst)
{
	t_list	*cur;
	size_t	i;

	cur = lst;
	i = 0;
	while (cur)
	{
		i++;
		cur = cur->next;
	}
	return (i);
}
